import turtle

import turtle as my_turtle


# Set up the screen
screen = my_turtle.Screen()
screen.title("Turtle Graphics Example")

# Create a turtle
my_turtle = turtle.Turtle()

# Move the turtle
my_turtle.forward(100)
my_turtle.right(90)
my_turtle.forward(100)
my_turtle.right(90)
my_turtle.forward(100)
my_turtle.right(90)
my_turtle.forward(100)

# Finish
turtle.done()
